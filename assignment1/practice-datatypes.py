# 1. Data Type Conversions
print("Convert 3.75 to integer:", int(3.75))
print("Convert '123' to float:", float("123"))
print("Convert 0 to boolean:", bool(0))
print("Convert False to string:", str(False))

# 2. Uppercase conversion
x = "hello"
print("Uppercase:", x.upper())

# 3. Add x + y
x = 5
y = 3.14
z = x + y
print("z =", z, ", type =", type(z))
print("z as integer:", int(z))

# 4. String operations
s = "hello"
print("Uppercase:", s.upper())
print("Replace e -> a:", s.replace('e', 'a'))
print("Starts with 'he':", s.startswith('he'))
print("Ends with 'lo':", s.endswith('lo'))

